package module2;

public class Iterators {
	public static void main(String[] args) {
		// for loop
		for (int i = 0; i < 5; i++) {
			System.out.println("Hi Shubham");
		}
		System.out.println("************");

		// while loop
		int m = 0;
		while (m < 5) {
			System.out.println("Welcome to Intellij");
			m++;
		}
		System.out.println("##########");

		// do -while loop
		int v = 0;
		do {
			System.out.println("First code");
			v++;
		} while (v < 5);
	}
}
