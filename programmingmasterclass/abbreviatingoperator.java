package programmingmasterclass;

public class abbreviatingoperator {
public static void main(String[] args) {
	int result=34;
	System.out.println(result);
	result++;
	System.out.println(result);
	++result;
	System.out.println(result);
	result+=10;
	System.out.println(result);
	result--;
	System.out.println(result);
	--result;
	System.out.println(result);
	result-=15;
	System.out.println(result);
	result*=5;
	System.out.println(result);
	result/=45;
	System.out.println(result);
}
}
